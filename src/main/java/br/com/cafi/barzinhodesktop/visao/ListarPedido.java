    private List<Pedido> lista;

    private void carregarTabela() {
        limparTabela();
        PedidoBO bo = new PedidoBO();
        lista = bo.findAll();
        DefaultTableModel model
                = (DefaultTableModel) jTable1.getModel();
        for (Pedido obj : lista) {
            model.addRow(new Object[]{obj.getId(), obj.getObservacao(),
                obj.getStatus(), obj.getComanda() == null ? "Delivery" : obj.getComanda().getNome(), obj.getCliente() == null ? "sem cliente" : obj.getCliente().getNome(), obj.getTipo()});
        }
    }

    private void limparTabela() {
        DefaultTableModel model
                = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(0);
        }
    }

