    private void repor() {

        produtoSelecionado.setQuantidadeEstoque(
                produtoSelecionado.getQuantidadeEstoque()
                + Double.parseDouble(quantidadeTextField.getText()));
        if (JOptionPane.showConfirmDialog(null,
                "Deseja adcionar ao estoque de " + produtoSelecionado.getDescricao()
                + " uma quantidade de " + quantidadeTextField.getText()) == JOptionPane.YES_OPTION) {
            ProdutoBO bo = new ProdutoBO();
            bo.update(produtoSelecionado);

        }
    }

    private void limparTabela() {
        DefaultTableModel modelo = (DefaultTableModel) produtoTable.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
