    private void produtoTableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_produtoTableMouseReleased
        DefaultTableModel modelo = (DefaultTableModel) produtoTable.getModel();
        Long id = (Long) modelo.getValueAt(produtoTable.getSelectedRow(), 0);
        codigoTextField.setText(id+"");
        atualizarDadosDoProdutoSelecionado();
    }//GEN-LAST:event_produtoTableMouseReleased

    private void atualizarDadosDoProdutoSelecionado(){
        ProdutoBO bo = new ProdutoBO();
        produtoSelecionado = bo.getById(Long.parseLong(codigoTextField.getText()));
        produtoLabel.setText("Produto: " + produtoSelecionado.getDescricao());
    }
    
